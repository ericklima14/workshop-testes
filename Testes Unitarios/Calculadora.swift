import Foundation

class Calculadora {
    func soma(a: Double, b: Double) -> Double{
        return a + b
    }
    
    func sub(a: Double, b: Double) -> Double{
        return a - b
    }
    
    func mul(a: Double, b: Double) -> Double{
        return a * b
    }
    
    func div(a: Double, b: Double) -> Double{
        return a / b
    }
}
